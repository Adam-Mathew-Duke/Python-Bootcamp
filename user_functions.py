# creating your own functions in Python

# create the function
def HelloWorld():
    print("Hello World")

def PrintName(name):
    print('Hi '+name+'!')

def printSum(num1,num2):
    print(num1+num2)

def returnSum(num1,num2):
    return(num1+num2)

# call the function
HelloWorld()
PrintName("Adam")
printSum(5,10)
print(returnSum(5,10))

